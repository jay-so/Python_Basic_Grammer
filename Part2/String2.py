#문자열 안에 작은 따옴표나 큰따옴표를 표함시키고 싶을 때

#1. 문자열에 작은 따옴표(') 포함시키기
food = "Python's favorite food is perl"
print(food)

#2. 문자열에 큰따옴표(") 포함시키기
say = '"Python is very easy" he says'
print(say)

#3.\(백슬러시)를 이용하여 작은따욤표(')와 큰따옴표(")를 문자열에 포함시키기
food = 'Python\'s favorite food is perl'
print(food)
say = "\"Python is very easy.\" he says."
print(say)

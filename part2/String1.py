#문자열 생성
#1. 큰 따옴표(")로 양쪽 둘러싸기
str = "Hello World"
print(str)

#2. 작은 따옴표(')로 양쪽 둘러싸기
str2 = 'Python is fun'
print(str2)

#3. 큰따옴표 3개를 연속(""")으로 써서 양쪽 둘러싸기
str3 = """Life is too short, You need python"""
print(str3)

#4. 작은 따옴표 3개를 연속(''')으로 써서 양쪽 둘러싸기
str4 = '''Life is too short. You need Python'''
print(str4)